    private void btnTraSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraSachActionPerformed

       int selectedRow = tblPhieuMuon.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui long chon 1 dong!");
            return;
        }
        DICHVU_DAO dao = new DICHVU_DAO();

        String spm = tblPhieuMuon.getValueAt(selectedRow, 0).toString();
        String trangThai = tblPhieuMuon.getValueAt(selectedRow, 5).toString();
        if (spm.trim().isEmpty()) {
            warn("So phieu muon khong duoc de trong!");
            return;
        }
        if (getMsnvDN().trim().isEmpty() || getMsnvDN() == null) {
            warn("Ma so nhan vien khong duoc de trong!");
            return;
        }
        if (trangThai.equalsIgnoreCase("Da tra") || trangThai.equalsIgnoreCase("Bi phat")) {
            warn("Phieu muon da duoc tra!");
            return;
        }

        try {
            String mess = dao.traSach(spm, getMsnvDN());
            JOptionPane.showMessageDialog(this, mess);
        } catch (Exception ex) {
            Logger.getLogger(Bao_cao.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            loadTablePhieuMuon();
        } catch (Exception ex) {
            Logger.getLogger(Bao_cao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTraSachActionPerformed

    private void btnTKPPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKPPActionPerformed
        String spp = JOptionPane.showInputDialog(this, "Nhap so phieu phat: ", "Tim kiem", JOptionPane.QUESTION_MESSAGE);

        DefaultTableModel model = (DefaultTableModel) tblPhieuPhat.getModel();

        if (spp == null || spp.trim().isEmpty()) {
            return; // thoát không làm gì
        }

        boolean found = false;
        for (int i = 0; i < model.getRowCount(); i++) {
            String value = model.getValueAt(i, 0).toString();
            if (value.equalsIgnoreCase(spp.trim())) {
                // Chọn dòng và cuộn đến đó
                tblPhieuPhat.setRowSelectionInterval(i, i);
                tblPhieuPhat.scrollRectToVisible(tblPhieuPhat.getCellRect(i, 0, true));
                found = true;
                break;
            }
        }

        if (!found) {
            JOptionPane.showMessageDialog(this, "Khong tim thay phieu phat co ma: " + spp, "Ket qua", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnTKPPActionPerformed

