    private void btnTKPTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKPTActionPerformed
        // TODO add your handling code here:
        String spm = JOptionPane.showInputDialog(this, "Nhap so phieu muon: ", "Tim kiem", JOptionPane.QUESTION_MESSAGE);

        DefaultTableModel model = (DefaultTableModel) tblPhieuTra.getModel();

        if (spm == null || spm.trim().isEmpty()) {
            return; // thoát không làm gì
        }

        tblPhieuTra.clearSelection(); // Xóa chọn cũ
        boolean found = false;

        for (int i = 0; i < model.getRowCount(); i++) {
            String value = model.getValueAt(i, 0).toString();
            if (value.equalsIgnoreCase(spm.trim())) {
                // Chọn nhiều dòng cùng lúc
                tblPhieuTra.addRowSelectionInterval(i, i);
                found = true;
            }
        }

// Nếu muốn cuộn đến dòng đầu tiên tìm thấy
        if (found) {
            int firstIndex = tblPhieuTra.getSelectedRows()[0];
            tblPhieuTra.scrollRectToVisible(tblPhieuTra.getCellRect(firstIndex, 0, true));
        }

        if (!found) {
            JOptionPane.showMessageDialog(this, "Khong tim thay phieu muon co ma: " + spm, "Ket qua", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnTKPTActionPerformed

