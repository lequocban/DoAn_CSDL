    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) // ko chọn dòng nào 
        {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên để xóa!");
            return;
        } else {
            Object[] options = {"Yes", "No"};

            int choice = JOptionPane.showOptionDialog(this, "Bạn có muốn xóa không", "Xóa dữ liệu", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);

            if (choice == JOptionPane.YES_OPTION) {
                NHANVIEN_DAO nv = new NHANVIEN_DAO();
                String msnv = jTable1.getValueAt(selectedRow, 0).toString();
                try {
                    String mess = nv.xoaNV(msnv);
                    JOptionPane.showMessageDialog(this, mess);
                } catch (Exception ex) {
                    Logger.getLogger(Nhan_vien.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    initTable();
                } catch (Exception ex) {
                    Logger.getLogger(Nhan_vien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

