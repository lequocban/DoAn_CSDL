    private void initTable() throws Exception {
        model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{
            "Mã nhân viên", "Họ và tên", "Địa chỉ", "Ngày sinh", "Giới tính", "Số điện thoại", "Email", "Ngày vào làm", "Chức vụ"
        });
        NHANVIEN_DAO nv_DAO = new NHANVIEN_DAO();
        ArrayList<NHANVIEN> lstNV = new ArrayList<>();
        
        lstNV = nv_DAO.getDSNV();
        SimpleDateFormat dinhDang = new SimpleDateFormat("dd/MM/yyyy");
        for (NHANVIEN nv :lstNV){
            String [] data = {nv.getMSNV(), nv.getHoTenNV(), nv.getDiaChiNV(),
                dinhDang.format(nv.getNgaySinhNV()), nv.getGioiTinh(),
                nv.getSdtNV(), nv.getEmailNV(),
                dinhDang.format(nv.getNgayVaoLam()), nv.getChucVu()};
            model.addRow(data);
        }
        
        
        jTable1.setModel(model);
    }
