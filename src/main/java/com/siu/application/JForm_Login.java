private void checkLogin() throws Exception {
        NHANVIEN_DAO nv_DAO = new NHANVIEN_DAO();
        ArrayList<NHANVIEN> lstNV = new ArrayList<>();
        lstNV = nv_DAO.getDSNV();
        JPasswordField realmk = new JPasswordField("123");
        String tk = txtTenDangNhap.getText().trim();
        char[] mk = txtPassword.getPassword();
        char[] confirmmk = realmk.getPassword();

        if (tk.isEmpty()) {
            warn("Khong de trong ma so nhan vien!");
            return;
        }
        if (mk.length == 0) {
            warn("Mat khau khong duoc de trong!");
            return;
        }
        boolean ktra = false;
        for (NHANVIEN nv : lstNV) {
            if (tk.equals(nv.getMSNV()) && Arrays.equals(mk, confirmmk)) {
                msnvDN = nv.getMSNV();
                ktraDN = true;
                JOptionPane.showMessageDialog(this, "Login success!", "Success", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        warn("Wrong email or password!");

    }

