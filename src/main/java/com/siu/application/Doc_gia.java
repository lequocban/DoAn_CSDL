    private void initTable() throws Exception {
    // Tạo model cho JTable
    DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
    model.setRowCount(0);
    model.setColumnIdentifiers(new Object[]{
        "MSDG", "Họ và tên", "Địa chỉ", "Ngày sinh", "Email", "Giới tính", "Ngày đăng ký", "Nghề nghiệp"
    });

    // Lấy danh sách độc giả từ DAO
    DOCGIA_DAO dg_DAO = new DOCGIA_DAO();
    ArrayList<DOCGIA> lstDG = new ArrayList<>();
    lstDG = dg_DAO.getDSDG();
    // Định dạng ngày
    SimpleDateFormat dinhDang = new SimpleDateFormat("dd/MM/yyyy");

    // Duyệt danh sách độc giả và thêm vào bảng
    for (DOCGIA dg : lstDG) {
        String[] data = {
            dg.getMSDG(),
            dg.getTenDG(),
            dg.getDiaChi(),
            dinhDang.format(dg.getNgaySinh()),
            dg.getEmail(),
            dg.getGioiTinh(),
            dinhDang.format(dg.getNgayDK()),
            dg.getThongTinKhac()
        };
        System.out.println(dg);
        model.addRow(data);
    }

    // Gán model vào JTable
    jTable1.setModel(model);
}

    
