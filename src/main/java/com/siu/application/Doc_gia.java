    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String keyword = JOptionPane.showInputDialog(this, "Nhập mã độc giả cần tìm:");
        boolean found = false;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).toString().equalsIgnoreCase(keyword)) {
                jTable1.setRowSelectionInterval(i, i);
                found = true;
                break;
            }
        }

        if (!found) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy độc giả có mã: " + keyword);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) // ko chọn dòng nào 
        {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn đọc giả để xóa!");
            return;
        } else {
            Object[] options = {"Yes", "No"};

            int choice = JOptionPane.showOptionDialog(this, "Bạn có muốn xóa không", "Xóa dữ liệu", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);

            if (choice == JOptionPane.YES_OPTION) {
                DOCGIA_DAO dg_DAO = new DOCGIA_DAO();
                String msdg = jTable1.getValueAt(selectedRow, 0).toString();
                try {
                    String mess = dg_DAO.xoaDG(msdg);
                    JOptionPane.showMessageDialog(this, mess);
                } catch (Exception ex) {
                    Logger.getLogger(Doc_gia.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    initTable();
                } catch (Exception ex) {
                    Logger.getLogger(Doc_gia.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

